package com.symteo.domain.counsel.enums;

import lombok.Getter;
import lombok.RequiredArgsConstructor;

@Getter
@RequiredArgsConstructor
public enum Answer_Format{
    SITUATIONAL(
            "situational",
            """
            [답변 길이/호흡 규칙]
            - 사용자의 입력 길이에 따라 응답 길이를 자동 조절한다.
            - 단문(1~2문장): 공감 1문장 + 질문 1문장(총 2~3문장 이내).
            예시)
                - 사용자: "오늘 진짜 최악이었어."
                - AI: "정말 힘든 하루였나 보네요... 무슨 일이 00님을 그렇게 힘들게 했는지 말해줄 수 있나요?"
            - 중문(3~4문장): 공감 1문장 + 상황정리 1문장 + 질문 1문장(총 3문장 권장).
            예시)
                - 사용자: "친구랑 약속을 잡았는데 갑자기 취소됐어. 나만 기다린 것 같아서 서운하고, 그냥 혼자 있고 싶어."
                - AI: "미리 준비하며 기다렸을 텐데 갑작스러운 취소에 많이 서운하셨겠어요.
                기대가 컸던 만큼 실망도 크신 것 같은데, 지금은 누구를 만나는 것보다 혼자만의 시간이 더 필요하신 걸까요?"
            - 장문(5문장 이상/구체 사연): 충분한 분량으로 정성스럽게, 사용자가 말한 핵심 키워드를 짚어 공감/정리 후 질문 1~2개로 마무리.
            예시)
                - 사용자: (회계 업무 실수와 상사의 질책, 동료들의 시선에 대한 긴 사연)
                - AI: "하나하나 읽어보니 00님이 오늘 느끼셨을 당혹감과 수치심이 얼마나 컸을지 짐작조차 되지 않네요.
                업무 실수 자체보다도 믿었던 상사분의 질책과 주변의 시선이 00님을 더 아프게 찌른 것 같아요.
                그런 상황에서도 끝까지 자리를 지키고 돌아오신 것만으로도 대단한 일이에요. 지금 가장 마음에서 떠나지 않는 장면이 있다면 무엇인가요?"
            """
    ),

    SHORT_FORMAT(
            "short_format",
            """
            [답변 형식: 짧고 간결]
            - 핵심만 담아 2~3문장 이내로 답한다.
            - 공감 1문장 + 핵심 질문 1문장(필요 시 상황 확인 1문장 추가)으로 구성한다.
            예시) "많이 지치셨나 봐요. 지금 가장 힘든 점 한 가지만 저에게 말씀해 주실 수 있나요?"
            """
    ),

    LONG_FORMAT(
            "long_format",
            """
            [답변 형식: 길고 자세히]
            - 심층 분석 + 상세 가이드를 충분한 분량으로 제공한다.
            - 공감(감정/상황 반영) → 요약/정리 → 선택지/가이드(구체적 제안) → 질문 순서로 구성한다.
            예시) "잠을 못 자면 신체적으로도 힘들지만 심리적으로 더 위축되기 마련이에요.
            00님의 이야기를 들어보니 그동안 혼자 감당하려던 무게가 너무 컸던 것 같아 마음이 아프네요.
            오늘 밤에는 조금이라도 편히 쉴 수 있도록, 저와 함께 머릿속을 복잡하게 만드는 생각들을 하나씩 꺼내서 정리해볼까요?"
            """
    );

    private final String code;        // DB 저장/표시용
    private final String promptText;  // LLM에 주입할 규칙 텍스트
}